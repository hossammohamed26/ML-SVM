y_data=dataset['target']
print(x_data)
print(y_data)

#convert to matrix
x_data=np.array(x_data)
y_data=np.array(y_data).flatten()
print(x_data.shape)
print(y_data.shape)

x_data=normalization(x_data)
print(x_data)

xTrain, xTest, yTrain, yTest = split(x_data, y_data)
